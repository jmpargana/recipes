<h1>About</h1>

<span>Why Share Your Recipes?</span>
<p>
	You must be thinking: what's the point of yet another recipe website. It seems there is no
	gastronomic entity backing up this project, nor is its dataset substantial. Share Your Recipes is
	a simple project that solves the following 4 points.
</p>

<ul>
	<li>Tag-based Search</li>
	<li>Zero Bloat</li>
	<li>Blazing Fast</li>
	<li>Community and Developer Friendly</li>
</ul>

<section>
	<span>Tag-based Search</span>
	<p>
		Have you ever tried organizing your recipes in a consistent hierarchical matter and failed to do
		so? The problem with the traditional Tree-based cathegorization is that you quickly start
		modeling ...
	</p>
</section>

<section>
	<span>Zero Bload</span>
	<p>
		If you've used different recipe websites you're probably aware of the insane amount of adds and
		trackers running them. In ShareYourRecipes not only do you have zero external javascript
		running, as the app itself is quite minimal and sticks to showing nothing more than the recipes
		themselves.
	</p>
</section>
<section>
	<span>Blazing Fast</span>
	<p>
		Written on top of svelte and golang, the tech tools used to produce this piece of software are
		at the time of writing among the fastest available. Svelte is the fastest to load and run, and
		fasthttp is the fastest to respond. This garantees users can make the best out of slow internet
		connections and low performance hardware, such as old phones.
	</p>
</section>
<section>
	<span>Community and Developer Friendly</span>
	<p>
		Since this is an Open Source Licensed Project, anyone can easily contribute by fixing the
		documentation or creating extra features. In this website not only will you have a simple way of
		storing and searching your recipes, as you'll have the possibility to share and access others.
	</p>
</section>
