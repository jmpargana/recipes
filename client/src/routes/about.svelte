<h2 class="space-bottom">About</h2>

<h5 class="space-bottom-xl primary">Welcome to ShareYourRecipes!</h5>
<p>I'll explain how the idea was born and what problems it solves.</p>
<p>Do you often eat unhealthy foods or spend money in take away because you didn't plan what to cook ahead?</p>
<p>Even though I love to cook, I've certainly fallen into this habit. The reason is simple. Lack of organization. Both in deciding what to cook, as in having the ingridients at home. </p>
<p>With this app, you can store all of your favorite recipes and search for your cravings using tag-based search. Instead of categorizing your recipes by cuisine (italian, indian) or by dish (soup, starter, dessert), you can do both. Create all the tags you can think of to simplify the search.</p>
<p>Example: let's say you have a delicious italian fish soup. Use the navbar to create a <a href="#/new-recipe">New Recipe</a> and add the tags <b>soup</b>, <b>italian</b> and <b>fish</b>. Now you can find it if you're craving italian, if you want to have a soup in your meal prep, or if you want to eat some sea food.</p>


